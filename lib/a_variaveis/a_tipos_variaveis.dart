void main() {
  // Tipos
  // String, int, double, bool

  // Declarações
  String curso = 'Academia do Flutter';
  int acessoEmAnos = 2;
  double valor = 1196.2;
  bool turmaAberta = false;

  // Tipos Nulos
  // Todos os Tipos podem recever 2 valores (NonNull e Null)
  String? cursoNulo;
  int? acessoEmAnosN;
  double? valorN;
  bool? turmaAbertaN;

  print([
    curso,
    acessoEmAnos,
    valor,
    turmaAberta,
    cursoNulo,
    acessoEmAnosN,
    valorN,
    turmaAbertaN
  ]);
}
